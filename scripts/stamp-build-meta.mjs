import { execFile } from "node:child_process";
import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { promisify } from "node:util";

const execFileAsync = promisify(execFile);
const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, "..");
const outputPath = path.join(repoRoot, "src", "buildMeta.js");

const gitMeta = await readLatestGitCommitMeta();
const nextContent = buildMetaModuleSource(gitMeta);
const previousContent = await readTextFile(outputPath);

if (previousContent !== nextContent) {
  await writeFile(outputPath, nextContent, "utf8");
  console.log(`Updated ${path.relative(repoRoot, outputPath)}`);
} else {
  console.log(`No change in ${path.relative(repoRoot, outputPath)}`);
}

async function readLatestGitCommitMeta() {
  try {
    const { stdout } = await execFileAsync(
      "git",
      ["log", "-1", "--format=%H%n%h%n%cI"],
      { cwd: repoRoot },
    );
    const [fullHash = "", shortHash = "", committedAtIso = ""] =
      stdout.trim().split(/\r?\n/);
    return {
      commitHash: normalizeText(fullHash),
      shortHash: normalizeText(shortHash),
      committedAtIso: normalizeText(committedAtIso),
    };
  } catch {
    return {
      commitHash: "",
      shortHash: "",
      committedAtIso: "",
    };
  }
}

function buildMetaModuleSource(meta) {
  const payload = {
    commitHash: normalizeText(meta?.commitHash),
    shortHash: normalizeText(meta?.shortHash),
    committedAtIso: normalizeText(meta?.committedAtIso),
  };
  const payloadJson = JSON.stringify(payload, null, 2);

  return `// Auto-generated by scripts/stamp-build-meta.mjs.
// Do not edit this file manually.

export const BUILD_META = Object.freeze(${payloadJson});

export function formatBuildLabel(meta = BUILD_META) {
  const shortHash = normalizeLabelText(meta?.shortHash);
  const timestamp = formatBuildTimestamp(meta?.committedAtIso);
  if (timestamp && shortHash) {
    return \`Uppdaterad: \${timestamp} | \${shortHash}\`;
  }
  if (timestamp) {
    return \`Uppdaterad: \${timestamp}\`;
  }
  if (shortHash) {
    return \`Version: \${shortHash}\`;
  }
  return "Version okand";
}

function formatBuildTimestamp(value) {
  const isoValue = normalizeLabelText(value);
  if (!isoValue) {
    return "";
  }
  const timestamp = new Date(isoValue);
  if (!Number.isFinite(timestamp.getTime())) {
    return "";
  }
  return [
    String(timestamp.getFullYear()).padStart(4, "0"),
    String(timestamp.getMonth() + 1).padStart(2, "0"),
    String(timestamp.getDate()).padStart(2, "0"),
  ].join("-") + " " + [
    String(timestamp.getHours()).padStart(2, "0"),
    String(timestamp.getMinutes()).padStart(2, "0"),
  ].join(":");
}

function normalizeLabelText(value) {
  if (typeof value !== "string") {
    return "";
  }
  return value.trim();
}
`;
}

async function readTextFile(filePath) {
  try {
    return await readFile(filePath, "utf8");
  } catch {
    return "";
  }
}

function normalizeText(value) {
  if (typeof value !== "string") {
    return "";
  }
  return value.trim();
}
